a = 5
b = 3

print(a == b)  # Saída: False, porque 5 não é igual a 3
print(a != b)  # Saída: True, porque 5 é diferente de 3
print(a < b)   # Saída: False, porque 5 não é menor que 3
print(a > b)   # Saída: True, porque 5 é maior que 3
print(a <= b)  # Saída: False, porque 5 não é menor ou igual a 3
print(a >= b)  # Saída: True, porque 5 é maior ou igual a 3